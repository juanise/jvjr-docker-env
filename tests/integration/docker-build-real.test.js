/**
 * Docker Real Build Integration Tests - Phase 4
 *
 * Verifies the full Docker lifecycle:
 * 1. Create a minimal frontend project
 * 2. Apply jvjr-docker-env (generates Dockerfile, entrypoint, jvjr-env.json)
 * 3. Run docker build (real image build)
 * 4. Run docker run and verify env var substitution works
 * 5. Cleanup image
 *
 * Requires: Docker daemon running
 * Timeout: 120s per test (image pull + build)
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { execSync, execFileSync } from 'child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const PROJECT_ROOT = path.resolve(__dirname, '../..');
const DOCKER_BUILD_DIR = path.resolve(PROJECT_ROOT, 'tests/integration/docker-build-real');
const IMAGE_PREFIX = 'jvjr-test';

// --- Docker availability check ---
function checkDockerAvailable() {
    try {
        execSync('docker info', { stdio: 'ignore', timeout: 5000 });
        return true;
    } catch {
        return false;
    }
}

const DOCKER_AVAILABLE = checkDockerAvailable();

function runCli(args, cwd) {
    return execSync(
        `node ${path.join(PROJECT_ROOT, 'bin/jvjr')} ${args}`,
        { cwd, encoding: 'utf-8', timeout: 10000 }
    );
}

function dockerBuild(imageTag, contextDir) {
    return execSync(
        `docker build -t ${imageTag} -f Dockerfile-jvjr .`,
        { cwd: contextDir, encoding: 'utf-8', timeout: 180000 }
    );
}

function dockerRun(imageTag, envVars, cmd) {
    const envFlags = Object.entries(envVars)
        .map(([k, v]) => `-e "${k}=${v}"`)
        .join(' ');
    return execSync(
        `docker run --rm ${envFlags} --entrypoint sh ${imageTag} -c "${cmd}"`,
        { encoding: 'utf-8', timeout: 30000 }
    );
}

function dockerRmi(imageTag) {
    try {
        execSync(`docker rmi ${imageTag}`, { stdio: 'ignore', timeout: 15000 });
    } catch {
        // ignore cleanup failures
    }
}

function imageExists(imageTag) {
    try {
        execSync(`docker image inspect ${imageTag}`, { stdio: 'ignore', timeout: 5000 });
        return true;
    } catch {
        return false;
    }
}

// --- Build script restoration after jvjr --start ---
// jvjr --start prepends "jvjr --build && " to the build script.
// Inside Docker, jvjr is not installed (it's a host tool), so we restore
// the build script to just "sh build.sh" after --start.
// jvjr-env.json was already generated by --start on the host.
function fixBuildScriptForDocker(projectDir, originalBuildScript) {
    const pkgPath = path.join(projectDir, 'package.json');
    const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf-8'));
    pkg.scripts.build = originalBuildScript;
    fs.writeFileSync(pkgPath, JSON.stringify(pkg, null, 2));
}

// --- Minimal Vue project factory ---
function createMinimalVueProject(projectDir, envVars) {
    fs.mkdirSync(path.join(projectDir, 'src'), { recursive: true });

    // package.json - build script uses build.sh (POSIX sh, no deps)
    fs.writeFileSync(
        path.join(projectDir, 'package.json'),
        JSON.stringify({
            name: 'vue-docker-test',
            version: '1.0.0',
            scripts: { build: 'sh build.sh' }
        }, null, 2)
    );

    // .env - Vue App vars
    const envContent = Object.entries(envVars)
        .map(([k, v]) => `${k}=${v}`)
        .join('\n') + '\n';
    fs.writeFileSync(path.join(projectDir, '.env'), envContent);

    // build.sh - creates dist with JS placeholders for runtime substitution
    // The JS file embeds $VUE_APP_* literals that entrypoint replaces at runtime
    const envKeys = Object.keys(envVars);
    const jsContent = envKeys
        .map(k => {
            const short = k.replace(/^VUE_APP_/, '');
            return `${short.toLowerCase()}:"$${k}"`;
        })
        .join(',');

    fs.writeFileSync(
        path.join(projectDir, 'build.sh'),
        [
            '#!/bin/sh',
            'mkdir -p dist/js',
            `printf 'window.config={${jsContent}};' > dist/js/app.main.js`,
            "printf '<html><body><script src=\"/js/app.main.js\"></script></body></html>' > dist/index.html"
        ].join('\n') + '\n'
    );
}

