maybeDescribe('Phase 4: Docker Runtime - Env Var Isolation', () => {
    jest.setTimeout(180000);

    const imageTag = `${IMAGE_PREFIX}-isolation-${Date.now()}`;
    const projectDir = path.join(DOCKER_BUILD_DIR, 'isolation-app');

    beforeAll(() => {
        if (fs.existsSync(projectDir)) {
            fs.rmSync(projectDir, { recursive: true, force: true });
        }
        fs.mkdirSync(projectDir, { recursive: true });
        createMinimalVueProject(projectDir, {
            VUE_APP_SECRET: 'build-time-value',
            VUE_APP_PUBLIC: 'build-time-public'
        });
        runCli('--start', projectDir);
        fixBuildScriptForDocker(projectDir, 'sh build.sh');
        dockerBuild(imageTag, projectDir);
    });

    afterAll(() => {
        dockerRmi(imageTag);
    });

    test('runtime env var overrides build-time placeholder', () => {
        const cmd = 'cp /usr/share/nginx/html/js/app.main.js /tmp/app.main.js && envsubst < /tmp/app.main.js';
        const output = dockerRun(imageTag, {
            VUE_APP_SECRET: 'runtime-secret-value',
            VUE_APP_PUBLIC: 'runtime-public-value'
        }, cmd);
        expect(output).toContain('runtime-secret-value');
        expect(output).toContain('runtime-public-value');
        expect(output).not.toContain('build-time-value');
        expect(output).not.toContain('BUILD_PLACEHOLDER');
    });

    test('unset runtime vars leave placeholder empty string', () => {
        // envsubst replaces unset vars with empty string
        const cmd = 'cp /usr/share/nginx/html/js/app.main.js /tmp/app.main.js && envsubst < /tmp/app.main.js';
        const output = dockerRun(imageTag, { VUE_APP_SECRET: 'only-secret' }, cmd);
        expect(output).toContain('only-secret');
        // VUE_APP_PUBLIC should be replaced by empty string (envsubst behavior for unset vars)
        expect(output).not.toContain('$VUE_APP_PUBLIC');
    });

    test('different container runs are isolated (no shared state)', () => {
        const cmd = 'cp /usr/share/nginx/html/js/app.main.js /tmp/app.main.js && envsubst < /tmp/app.main.js';

        const run1 = dockerRun(imageTag, { VUE_APP_SECRET: 'run-one-value', VUE_APP_PUBLIC: 'pub' }, cmd);
        const run2 = dockerRun(imageTag, { VUE_APP_SECRET: 'run-two-value', VUE_APP_PUBLIC: 'pub' }, cmd);

        expect(run1).toContain('run-one-value');
        expect(run2).toContain('run-two-value');
        expect(run1).not.toContain('run-two-value');
        expect(run2).not.toContain('run-one-value');
    });
});

maybeDescribe('Phase 4: Docker Build - Uninstall Lifecycle', () => {
    jest.setTimeout(180000);

    const imageTag = `${IMAGE_PREFIX}-lifecycle-${Date.now()}`;
    const projectDir = path.join(DOCKER_BUILD_DIR, 'lifecycle-app');

    beforeAll(() => {
        if (fs.existsSync(projectDir)) {
            fs.rmSync(projectDir, { recursive: true, force: true });
        }
        fs.mkdirSync(projectDir, { recursive: true });
        createMinimalVueProject(projectDir, { VUE_APP_BASE_URL: 'http://localhost' });
        runCli('--start', projectDir);
        fixBuildScriptForDocker(projectDir, 'sh build.sh');
    });

    afterAll(() => {
        dockerRmi(imageTag);
    });

    test('after --start, project can build a Docker image', () => {
        dockerBuild(imageTag, projectDir);
        expect(imageExists(imageTag)).toBe(true);
    });

    test('after --uninstall, Docker files are removed from project', () => {
        runCli('--uninstall', projectDir);
        expect(fs.existsSync(path.join(projectDir, 'Dockerfile-jvjr'))).toBe(false);
        expect(fs.existsSync(path.join(projectDir, 'jvjr-entrypoint.sh'))).toBe(false);
        expect(fs.existsSync(path.join(projectDir, 'jvjr-env.json'))).toBe(false);
    });

    test('after --uninstall, package.json scripts are cleaned', () => {
        const pkg = JSON.parse(
            fs.readFileSync(path.join(projectDir, 'package.json'), 'utf-8')
        );
        expect(pkg.scripts['jvjr-build']).toBeUndefined();
        if (pkg.scripts.build) {
            expect(pkg.scripts.build).not.toContain('jvjr');
        }
    });

    test('after --start again, project rebuilds Docker image', () => {
        runCli('--start', projectDir);
        fixBuildScriptForDocker(projectDir, 'sh build.sh');
        const newTag = `${imageTag}-rebuilt`;
        try {
            dockerBuild(newTag, projectDir);
            expect(imageExists(newTag)).toBe(true);
        } finally {
            dockerRmi(newTag);
        }
    });
});

