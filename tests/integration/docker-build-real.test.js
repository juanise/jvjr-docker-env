const maybeDescribe = DOCKER_AVAILABLE ? describe : describe.skip;

maybeDescribe('Phase 4: Docker Real Build - Vue App', () => {
    jest.setTimeout(180000);

    const imageTag = `${IMAGE_PREFIX}-vue-${Date.now()}`;
    const projectDir = path.join(DOCKER_BUILD_DIR, 'vue-app');

    const VUE_ENV_VARS = {
        VUE_APP_API_URL: 'http://build-time-placeholder.local',
        VUE_APP_TITLE: 'BUILD_PLACEHOLDER',
        VUE_APP_VERSION: 'BUILD_PLACEHOLDER_VERSION'
    };

    const RUNTIME_VARS = {
        VUE_APP_API_URL: 'https://prod.api.example.com',
        VUE_APP_TITLE: 'Production App',
        VUE_APP_VERSION: '3.2.1'
    };

    beforeAll(() => {
        if (fs.existsSync(projectDir)) {
            fs.rmSync(projectDir, { recursive: true, force: true });
        }
        fs.mkdirSync(projectDir, { recursive: true });
        createMinimalVueProject(projectDir, VUE_ENV_VARS);
        runCli('--start', projectDir);
        // jvjr is a host tool, not available inside Docker container.
        // jvjr-env.json is already generated; restore build script to sh build.sh only.
        fixBuildScriptForDocker(projectDir, 'sh build.sh');
        dockerBuild(imageTag, projectDir);
    });

    afterAll(() => {
        dockerRmi(imageTag);
    });

    test('docker image is built successfully', () => {
        expect(imageExists(imageTag)).toBe(true);
    });

    test('image contains jvjr-env.json at /jvjr-env.json', () => {
        const output = dockerRun(imageTag, {}, 'cat /jvjr-env.json');
        const envJson = JSON.parse(output);
        expect(envJson.API_URL).toBe('$VUE_APP_API_URL');
        expect(envJson.TITLE).toBe('$VUE_APP_TITLE');
        expect(envJson.VERSION).toBe('$VUE_APP_VERSION');
    });

    test('image contains compiled JS at /usr/share/nginx/html/js/app.main.js', () => {
        const output = dockerRun(imageTag, {}, 'cat /usr/share/nginx/html/js/app.main.js');
        // Should contain literal $VUE_APP_* placeholders (not yet substituted)
        expect(output).toContain('$VUE_APP_API_URL');
        expect(output).toContain('$VUE_APP_TITLE');
    });

    test('image contains index.html at /usr/share/nginx/html/index.html', () => {
        const output = dockerRun(imageTag, {}, 'cat /usr/share/nginx/html/index.html');
        expect(output).toContain('app.main.js');
    });

    test('jvjr-entrypoint.sh is executable in image', () => {
        const output = dockerRun(imageTag, {}, 'ls -la /jvjr-entrypoint.sh');
        expect(output).toContain('jvjr-entrypoint.sh');
        // Verify executable bit
        expect(output).toMatch(/^-rwx/m);
    });

    test('envsubst replaces VUE_APP_API_URL at runtime', () => {
        // envsubst without args replaces all env vars found in file
        const cmd = 'cp /usr/share/nginx/html/js/app.main.js /tmp/app.main.js && envsubst < /tmp/app.main.js';
        const output = dockerRun(imageTag, { VUE_APP_API_URL: RUNTIME_VARS.VUE_APP_API_URL }, cmd);
        expect(output).toContain('https://prod.api.example.com');
        expect(output).not.toContain('$VUE_APP_API_URL');
    });

    test('envsubst replaces all VUE_APP_* vars at runtime', () => {
        const cmd = 'cp /usr/share/nginx/html/js/app.main.js /tmp/app.main.js && envsubst < /tmp/app.main.js';
        const output = dockerRun(imageTag, RUNTIME_VARS, cmd);
        expect(output).toContain('https://prod.api.example.com');
        expect(output).toContain('Production App');
        expect(output).toContain('3.2.1');
        expect(output).not.toContain('$VUE_APP_');
    });

    test('nginx binary is available in image', () => {
        const output = dockerRun(imageTag, {}, 'nginx -v 2>&1 || true');
        expect(output).toMatch(/nginx/i);
    });
});

